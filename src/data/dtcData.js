// Get all unique brands
export const getBrands = () => {
  return dtcData.map((brand) => brand.brand);
};

// Get models by brand
export const getModelsByBrand = (brandName) => {
  const brand = dtcData.find((b) => b.brand === brandName);
  return brand ? brand.models : [];
};

// Get DTC codes by brand and model
export const getDTCByModel = (brandName, modelName) => {
  const brand = dtcData.find((b) => b.brand === brandName);
  if (!brand) return [];
  const model = brand.models.find((m) => m.name === modelName);
  return model ? model.dtcCodes : [];
};

// Get severity color
export const getSeverityColor = (severity) => {
  switch (severity) {
    case 'high':
      return 'bg-red-100 text-red-800 border-red-300';
    case 'medium':
      return 'bg-yellow-100 text-yellow-800 border-yellow-300';
    case 'low':
      return 'bg-green-100 text-green-800 border-green-300';
    default:
      return 'bg-gray-100 text-gray-800 border-gray-300';
  }
};

// Get severity badge background
export const getSeverityBadge = (severity) => {
  switch (severity) {
    case 'high':
      return 'bg-red-500';
    case 'medium':
      return 'bg-yellow-500';
    case 'low':
      return 'bg-green-500';
    default:
      return 'bg-gray-500';
  }
};

