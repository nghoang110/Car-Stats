export const getTotalCarCount = () => {
  return provincesData.reduce((sum, province) => sum + province.carCount, 0);
};

// Get provinces sorted by car count (descending)
export const getProvincesSorted = () => {
  return [...provincesData].sort((a, b) => b.carCount - a.carCount);
};

// Get provinces by region
export const getProvincesByRegion = (region) => {
  return provincesData.filter((p) => p.region === region);
};

// Get unique regions
export const getRegions = () => {
  return [...new Set(provincesData.map((p) => p.region))];
};
